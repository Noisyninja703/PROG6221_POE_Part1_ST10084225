using System;
using System.Collections;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PROG6221_POE_Part_1
{

    public class PopulateDictionary
    {

        //**************************************************************      Declare dictionary      *****************************************************************************************

        //Initialize the dictionary
        //KeySearch Dictionary Initialization
        public IDictionary<string, double> dictExpenses = new Dictionary<string, double>();

        //**************************************************************      Sum Dict      *****************************************************************************************

        //Calculate the sum of the values in the dictionary

        public double sumDict()
        {
            double sum = dictExpenses.Sum(x => x.Value);

            return sum;
        }

    }


